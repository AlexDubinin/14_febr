<?php
/**
8. Вывести на экран числа от -15 до 25 с помощью всех известных циклов.
 */

echo '<hr>Вывод чисел при помощи цикла for: <br>';
for ($i = -15; $i <= 25; $i++) {

    echo $i. '<br>';
}


echo '<hr>Вывод чисел при помощи цикла while: <br>';
$i = -15;
while ($i <=25) {
    echo $i. '<br>';
    $i++;
}


echo '<hr>Вывод чисел при помощи цикла do while: <br>';
$i = -15;
do {
    echo $i. '<br>';
    $i++;
} while ($i <25);

